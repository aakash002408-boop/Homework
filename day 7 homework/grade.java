class Grade 
{
	public static void main(String[] args)
	{
		int num = 82;
		
		if (num>=90)
		{
			System.out.println("Grade : A");
		}
		else if (num>=75)
		{
			System.out.println("Garde : B");
		}
		else if (num>=50)
		{
			System.out.println("Garde : C");
		}
		else if (num<=0)
		{
			System.out.println("fail");
		}
	}
}